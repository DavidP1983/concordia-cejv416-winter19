public class string {

    /**
     * @param args the command line arguments
     */
    public static void main(String[] args) {
       int first = 14;
       int second = 4;
       
       System.out.println(Integer.toString(first) + "+" + Integer.toString(second)+ " is " + Integer.toString(18));
       System.out.println(Integer.toString(first) + "*" + Integer.toString(second)+ " is " + Integer.toString(56));
       System.out.println(Integer.toString(first) + "%" + Integer.toString(second)+ " is " + Integer.toString(2));
       System.out.println(Integer.toString(first) + "/" + Integer.toString(second)+ " is " + Integer.toString(3));

    }
    
}
